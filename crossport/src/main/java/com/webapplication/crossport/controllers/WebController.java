package com.webapplication.crossport.controllers;

import com.webapplication.crossport.models.Product;
import com.webapplication.crossport.models.ProductRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestParam;

import java.util.Optional;

@Controller
public class WebController {

    @Autowired // This means to get the bean called userRepository. Which is auto-generated by Spring, we will use it to handle the data
    private ProductRepository productRepository;

    @GetMapping("/")
    public String greeting(@RequestParam(name="name", required=false, defaultValue="visitor") String name, Model model) {
        model.addAttribute("name", name);

        return "index";
    }

    @GetMapping("/shop")
    public String shop(Model model){

        Optional<Product> p = productRepository.findById(1);

        if(p.isPresent()){
            model.addAttribute("product", p.get());
        }

        return "shop";
    }

}
